#include <stdio.h>

int main(void) {
    int m, n;

    // Запрашиваем размеры прямоугольника
    printf("Введите количество строк (m): ");
    scanf("%d", &m);

    printf("Введите количество символов в строке (n): ");
    scanf("%d", &n);

    // Рисуем прямоугольник
    for (int i = 0; i < m; i++) {          // Внешний цикл для строк
        for (int j = 0; j < n; j++) {      // Внутренний цикл для символов в строке
            printf("*");
        }
        printf("\n");                      // Переход на новую строку
    }

    return 0;
}
